<template>
  <div id="app">
    <ValidationProvider
      ref="provider"
      :default-values="defaultValues"
      :default-errors="{
        'my-input': [{ message: 'outer error' }],
        'my.nested.value': [{ message: 'qwe' }]
      }"
      :resolver="$options.resolver"
      @submit="onSubmit"
    >
      <template #default="{ handleSubmit, values, dirty, errors, reset, onFieldChange }">
        <form @submit.prevent="handleSubmit">
          <label>Values</label>
          <pre>{{ values }}</pre>
          <label>Is dirty</label>
          <pre>{{ dirty }}</pre>
          <label>errors</label>
          <pre>{{ errors }}</pre>
          <ValidationField
            v-if="$options.get(values, 'my.nested.value') === 'test'"
            key="1"
            name="my-input"
          >
            <template #default="{ modelValue, onChange }">
              <input :value="modelValue" type="text" @input="onChange($event.target.value)" />
            </template>
          </ValidationField>
          <ValidationField v-else key="2" name="my-input">
            <template #default="{ modelValue, onChange }">
              <input :value="modelValue" type="text" @input="onChange($event.target.value)" />
            </template>
          </ValidationField>

          <ValidationField name="my.nested.value">
            <template #default="{ modelValue, onChange }">
              <input :value="modelValue" type="text" @input="onChange($event.target.value)" />
            </template>
          </ValidationField>

          <ValidationFieldArray name="arrayField">
            <template #default="{ name, fields, append, prepend, insert, swap, move, remove }">
              <div>
                <div v-for="(field, index) in fields" :key="field.id">
                  <ValidationField :name="`${name}.${index}.id`">
                    <template #default="{ modelValue, onChange }">
                      <input
                        :value="modelValue"
                        type="text"
                        @input="onChange($event.target.value)"
                      />
                    </template>
                  </ValidationField>
                  <ValidationField :name="`${name}.${index}.type`">
                    <template #default="{ modelValue, onChange }">
                      <input
                        :value="modelValue"
                        type="text"
                        @input="onChange($event.target.value)"
                      />
                    </template>
                  </ValidationField>
                  <ValidationField :name="`${name}.${index}.firstName`">
                    <template #default="{ modelValue, onChange }">
                      <input
                        :value="modelValue"
                        type="text"
                        @input="onChange($event.target.value)"
                      />
                    </template>
                  </ValidationField>
                </div>
                <button
                  type="button"
                  @click="
                    prepend({ firstName: 'prepend' }, { focusName: 'arrayField.0.firstName' })
                  "
                >
                  Prepend
                </button>
                <button type="button" @click="append({ firstName: 'append' })">Append</button>
                <button type="button" @click="insert(1, { firstName: 'insert' })">Insert</button>
                <button type="button" @click="swap(0, 2)">Swap</button>
                <button type="button" @click="move(0, 2)">Move</button>
                <button type="button" @click="remove(0)">Remove</button>
                <button type="button" @click="reset()">Reset</button>
              </div>
            </template>
          </ValidationFieldArray>

          <ValidationFieldArray name="bigArray">
            <template #default="{ name, fields, append, prepend, insert, swap, move, remove }">
              <div>
                <div v-for="(field, index) in fields" :key="field.id">
                  <ValidationField :name="`${name}.${index}.id`">
                    <template #default="{ modelValue, onChange }">
                      <input
                        :value="modelValue"
                        type="text"
                        @input="onChange($event.target.value)"
                      />
                    </template>
                  </ValidationField>
                  <ValidationField :name="`${name}.${index}.name`">
                    <template #default="{ modelValue, onChange }">
                      <input
                        :value="modelValue"
                        type="text"
                        @input="onChange($event.target.value)"
                      />
                    </template>
                  </ValidationField>
                </div>
                <button type="button" @click="prepend({ name: 'prepend' }, { field: 'name' })">
                  Prepend
                </button>
                <button type="button" @click="append({ name: 'append' })">Append</button>
                <button type="button" @click="insert(1, { name: 'insert' })">Insert</button>
                <button type="button" @click="swap(0, 2)">Swap</button>
                <button type="button" @click="move(0, 2)">Move</button>
                <button type="button" @click="remove(0)">Remove</button>
                <button type="button" @click="reset()">Reset</button>
                <button type="button" @click="handleSetBigArray">Set bigArray</button>
              </div>
            </template>
          </ValidationFieldArray>

          <ValidationErrors />
          <ValidationErrors name="my-input" />
          <button type="button" @click="onFieldChange('my-input', 123)">
            Set 'my-input' field
          </button>
          <button type="submit">Send</button>
        </form>
      </template>
    </ValidationProvider>
  </div>
</template>

<script>
import {
  ValidationField,
  ValidationProvider,
  ValidationFieldArray,
  ValidationErrors,
  get
} from './index';
import { nanoid } from 'nanoid';

const required = (value) => !!value;

export default {
  name: 'App',
  components: {
    ValidationProvider,
    ValidationField,
    ValidationFieldArray,
    ValidationErrors
  },
  get,
  resolver(values) {
    const result = {
      values,
      errors: {}
    };
    if (!required(get(values, 'my-input'))) {
      result.errors['my-input'] = [{ message: 'field required' }];
    }
    if (!required(get(values, 'my.nested.value'))) {
      result.errors['my.nested.value'] = [{ message: 'field required' }];
    }
    get(values, 'arrayField', []).forEach(({ firstName }, index) => {
      if (!required(firstName)) {
        result.errors[`arrayField.${index}.firstName`] = [{ message: 'field required' }];
      }
    });
    return result;
  },
  data() {
    return {
      defaultValues: {
        my: {
          nested: {
            value: 'test'
          }
        },
        arrayField: [
          {
            id: '1',
            firstName: '111',
            type: 'user'
          },
          {
            id: '2',
            firstName: '222',
            type: 'user'
          },
          {
            id: '3',
            firstName: '333',
            type: null
          }
        ],
        bigArray: Array.from(Array(350)).map(() => {
          const id = nanoid();
          return {
            id,
            name: `name-${id}`
          };
        })
      }
    };
  },
  methods: {
    onSubmit(values, { setError }) {
      setTimeout(() => {
        setError('my-input', { message: 'invalid field', type: 'custom' });
        setError('common', { message: 'invalid common field', type: 'custom' });
      }, 250);

      console.log(values);
    },
    handleSetBigArray() {
      this.$refs.provider.onFieldChange('bigArray', [
        this.$refs.provider.values.bigArray[4],
        this.$refs.provider.values.bigArray[2],
        this.$refs.provider.values.bigArray[7]
      ]);
    }
  }
};
</script>
